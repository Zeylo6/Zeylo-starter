/// Booking screen for completing a booking reservation
/// Based on Figma design "booking page"
class BookingScreen extends ConsumerStatefulWidget {
  /// Experience ID being booked
  final String experienceId;

  /// Experience title
  final String experienceTitle;

  /// Experience cover image URL
  final String experienceCoverImage;

  /// Host ID of the experience owner
  final String hostId;

  /// Total price for the booking
  final double totalPrice;

  const BookingScreen({
    required this.experienceId,
    required this.experienceTitle,
    required this.experienceCoverImage,
    required this.hostId,
    required this.totalPrice,
    super.key,
  });

  @override
  ConsumerState<BookingScreen> createState() => _BookingScreenState();
}

class _BookingScreenState extends ConsumerState<BookingScreen> {
  late TextEditingController _fullNameController;
  late TextEditingController _emailController;
  late TextEditingController _phoneController;

  String? _fullNameError;
  String? _emailError;
  String? _phoneError;
  String? _dateError;

  @override
  void initState() {
    super.initState();
    final user = ref.read(currentUserProvider).value;
    _fullNameController = TextEditingController(text: user?.displayName ?? '');
    _emailController = TextEditingController(text: user?.email ?? '');
    _phoneController = TextEditingController(text: user?.phoneNumber ?? '');

    // Update provider initial state
    WidgetsBinding.instance.addPostFrameCallback((_) {
      if (user != null) {
        final notifier = ref.read(bookingFormProvider.notifier);
        notifier.updateFullName(user.displayName);
        notifier.updateEmail(user.email);
        if (user.phoneNumber != null) {
          notifier.updatePhoneNumber(user.phoneNumber!);
        }
      }
    });
  }

  @override
  void dispose() {
    _fullNameController.dispose();
    _emailController.dispose();
    _phoneController.dispose();
    super.dispose();
  }

  void _validateForm() {
    final formState = ref.read(bookingFormProvider);

    setState(() {
      _fullNameError = Validators.validateName(formState.fullName);
      _emailError = Validators.validateEmail(formState.email);
      _phoneError = Validators.validatePhone(formState.phoneNumber);
      _dateError = formState.date.isEmpty ? 'Date is required' : null;
    });

    // Check if all validations passed
    if (_fullNameError == null &&
        _emailError == null &&
        _phoneError == null &&
        _dateError == null) {
      _submitBooking();
    } else {
      // Show feedback if validation fails
      ScaffoldMessenger.of(context).showSnackBar(
        const SnackBar(
          content: Text('Please correct the errors in the form.'),
          backgroundColor: AppColors.error,
        ),
      );
    }
  }

  Future<void> _submitBooking() async {
    final formNotifier = ref.read(bookingFormProvider.notifier);
    formNotifier.setLoading(true);

    try {
      final user = ref.read(currentUserProvider).value;
      if (user == null) {
        throw Exception('User details not found. Please try again.');
      }

      final formState = ref.read(bookingFormProvider);
      
      // Simplified date for now as we don't have a parser here
      DateTime bookingDate = DateTime.now();

      // 1. Initiate Stripe Payment
      if (mounted) {
        await StripePaymentService.makePayment(
          widget.totalPrice,
          '', // No booking ID yet
          formState.email, // Pass email for receipt
        );
      }

      // 2. Create the booking in Firestore ONLY after payment succeeds
      final booking = BookingEntity(
        id: '', // Generated by Firestore
        experienceId: widget.experienceId,
        experienceTitle: widget.experienceTitle,
        experienceCoverImage: widget.experienceCoverImage,
        userId: user.uid,
        hostId: widget.hostId,
        date: bookingDate,
        startTime: formState.time,
        guests: formState.guests,
        totalPrice: widget.totalPrice,
        status: 'pending', // Will be confirmed by host
        paymentStatus: 'paid', // Payment is already completed now
        createdAt: DateTime.now(),
        updatedAt: DateTime.now(),
        seekerName: _fullNameController.text.trim().isNotEmpty 
            ? _fullNameController.text.trim() 
            : (user.displayName.isNotEmpty ? user.displayName : 'Seeker'),
        seekerPhotoUrl: user.photoUrl,
      );

      final createBooking = ref.read(createBookingUseCaseProvider);
      await createBooking(booking);

      formNotifier.setLoading(false);
      if (mounted) {
        Navigator.pop(context);
        ScaffoldMessenger.of(context).showSnackBar(
          const SnackBar(
            content: Text('Booking and Payment successful!'),
            backgroundColor: AppColors.success,
          ),
        );
      }
    } catch (e) {
      formNotifier.setLoading(false);
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text('Error: $e'),
            backgroundColor: AppColors.error,
          ),
        );
      }
    }
  }

  @override
  Widget build(BuildContext context) {
    final formState = ref.watch(bookingFormProvider);

    return Scaffold(
      backgroundColor: AppColors.background,
      appBar: AppBar(
        backgroundColor: AppColors.background,
        elevation: 0,
        leading: Container(
          margin: const EdgeInsets.all(AppSpacing.sm),
          decoration: BoxDecoration(
            color: AppColors.primary,
            shape: BoxShape.circle,
          ),
          child: InkWell(
            onTap: () => Navigator.pop(context),
            child: const Icon(
              Icons.arrow_back,
              color: AppColors.textInverse,
              size: 24,
            ),
          ),
        ),
        title: Text(
          'Complete Your Booking',
          style: AppTypography.headlineSmall.copyWith(
            color: AppColors.primary,
          ),
        ),
        centerTitle: false,
      ),
      body: SingleChildScrollView(
        padding: const EdgeInsets.all(AppSpacing.lg),
        child: Column(
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            // Guest Information Section
            _buildSectionHeader('Guest Information'),
            const SizedBox(height: AppSpacing.lg),

            // Full Name
            ZeyloTextField(
              label: 'Full Name',
              hint: 'Enter your full name',
              controller: _fullNameController,
              errorText: _fullNameError,
              onChanged: (value) {
                ref.read(bookingFormProvider.notifier).updateFullName(value);
                if (_fullNameError != null) {
                  setState(() {
                    _fullNameError =
                        Validators.validateName(value);
                  });
                }
              },
            ),
            const SizedBox(height: AppSpacing.lg),

            // Email Address
            ZeyloTextField(
              label: 'Email Address',
              hint: 'Enter your email',
              controller: _emailController,
              keyboardType: TextInputType.emailAddress,
              errorText: _emailError,
              onChanged: (value) {
                ref.read(bookingFormProvider.notifier).updateEmail(value);
                if (_emailError != null) {
                  setState(() {
                    _emailError = Validators.validateEmail(value);
                  });
                }
              },
            ),
            const SizedBox(height: AppSpacing.lg),

            // Phone Number
            PhoneInputField(
              label: 'Phone Number',
              controller: _phoneController,
              errorText: _phoneError,
              onChanged: (value) {
                ref.read(bookingFormProvider.notifier).updatePhoneNumber(value);
                if (_phoneError != null) {
                  setState(() {
                    _phoneError = Validators.validatePhone(value);
                  });
                }
              },
            ),
            const SizedBox(height: AppSpacing.lg),

            // Number of Guests
            GuestSelector(
              label: 'Number of Guests',
              selectedGuests: formState.guests,
              onChanged: (value) {
                ref.read(bookingFormProvider.notifier).updateGuests(value);
              },
            ),
            const SizedBox(height: AppSpacing.xl),

            // Date and Time Section
            _buildSectionHeader('Select Date & Time'),
            const SizedBox(height: AppSpacing.lg),

            // Date Picker
            DatePicker(
              label: 'Date',
              selectedDate: formState.date,
              onChanged: (value) {
                ref.read(bookingFormProvider.notifier).updateDate(value);
                if (_dateError != null) {
                  setState(() {
                    _dateError = value.isEmpty ? 'Date is required' : null;
                  });
                }
              },
            ),
            if (_dateError != null) ...[
              const SizedBox(height: AppSpacing.xs),
              Text(
                _dateError!,
                style: AppTypography.bodySmall.copyWith(
                  color: AppColors.error,
                ),
              ),
            ],
            const SizedBox(height: AppSpacing.lg),

            // Time Picker
            TimePicker(
              label: 'Time',
              selectedTime: formState.time,
              onChanged: (value) {
                ref.read(bookingFormProvider.notifier).updateTime(value);
              },
            ),
            const SizedBox(height: AppSpacing.xl),

            // Summary Card
            _buildSummaryCard(),
            const SizedBox(height: AppSpacing.xl),

            // Complete Booking Button
            ZeyloButton(
              onPressed: _validateForm,
              label: 'Pay & Confirm Booking',
              variant: ButtonVariant.filled,
              isLoading: formState.isLoading,
              height: 52,
            ),
            const SizedBox(height: AppSpacing.lg),
            
            // Payment processed by Stripe tagline
            Center(
              child: Row(
                mainAxisAlignment: MainAxisAlignment.center,
                children: [
                  const Icon(Icons.lock_outline, size: 14, color: AppColors.textSecondary),
                  const SizedBox(width: 4),
                  Text(
                    'Payments are secure and encrypted by Stripe',
                    style: AppTypography.bodySmall.copyWith(color: AppColors.textSecondary),
                  ),
                ],
              ),
            ),
            const SizedBox(height: AppSpacing.xxl),
          ],
        ),
      ),
    );
  }

  Widget _buildSectionHeader(String title) {
    return Text(
      title,
      style: AppTypography.labelLarge.copyWith(
        fontWeight: FontWeight.bold,
      ),
    );
  }

  Widget _buildSummaryCard() {
    final formState = ref.watch(bookingFormProvider);

    return Container(
      padding: const EdgeInsets.all(AppSpacing.lg),
      decoration: BoxDecoration(
        color: AppColors.surface,
        borderRadius: BorderRadius.circular(AppRadius.md),
        border: Border.all(color: AppColors.border, width: 1),
      ),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Text(
            'Booking Summary',
            style: AppTypography.labelLarge.copyWith(
              fontWeight: FontWeight.bold,
            ),
          ),
          const SizedBox(height: AppSpacing.md),
          _summaryRow('Experience', widget.experienceTitle),
          const SizedBox(height: AppSpacing.sm),
          _summaryRow('Guests', '${formState.guests} ${formState.guests == 1 ? 'guest' : 'guests'}'),
          const SizedBox(height: AppSpacing.sm),
          _summaryRow('Date', formState.date),
          const SizedBox(height: AppSpacing.sm),
          _summaryRow('Time', formState.time),
          const Padding(
            padding: EdgeInsets.symmetric(vertical: AppSpacing.md),
            child: Divider(color: AppColors.divider, height: 1),
          ),
          _summaryRow(
            'Total Price',
            'Rs. ${widget.totalPrice.toStringAsFixed(0)}',
            isBold: true,
          ),
        ],
      ),
    );
  }

  Widget _summaryRow(String label, String value, {bool isBold = false}) {
    return Padding(
      padding: const EdgeInsets.symmetric(vertical: 4),
      child: Row(
        mainAxisAlignment: MainAxisAlignment.spaceBetween,
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          SizedBox(
            width: 100, // Fixed width for label to avoid layout issues
            child: Text(
              label,
              style: AppTypography.bodyMedium.copyWith(
                color: AppColors.textSecondary,
              ),
            ),
          ),
          const SizedBox(width: AppSpacing.md),
          Expanded(
            child: Text(
              value,
              textAlign: TextAlign.end,
              style: isBold
                  ? AppTypography.labelLarge.copyWith(
                      color: AppColors.primary,
                    )
                  : AppTypography.bodyMedium.copyWith(
                      color: AppColors.textPrimary,
                      fontWeight: FontWeight.w500,
                    ),
            ),
          ),
        ],
      ),
    );
  }
}
